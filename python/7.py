#operators and operands

#1.arithmetic operators

print(1+2)
print(2-1)
print(2*2)
print(4/2)
print(2**3)
print(8%3)
print(8//3)

#2.comparison operators

print(2<3)
print(2<=3)
print(5>2)
print(5>=3)
print(5==5)
print(5!=5)

#3.logical operators

print(3>2 and 4>2)
print(2>1 or 1>2)
print(not(2>1 and 3>2))

#4.Assignment operatos

score=0
score+=2 #score=score+2
score-=1
score*=3
print(score)

#5.identity operators

a=123
b="123"
print(a is b)

#6.bitwise operators

#AND(&) operator

print(bin(10))
print(10 & 8)

#OR(|) operator
print (10 | 8)

#XOR(^) operator
print(10 ^ 8)

#zero fill left shift
print(10 >> 2)

#zero fill right shift
print(10 << 2)


